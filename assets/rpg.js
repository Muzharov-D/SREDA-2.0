/* активные задачи человека: из досок отдела + текущий фокус ---------------- */
function questsOf(dept, name, task){
  const cfg = (typeof COCKPITS !== 'undefined') && COCKPITS[dept];
  const out = [];
  if (cfg && cfg.board && cfg.cols){
    const cols = cfg.cols.map(c => c[0]);
    Object.keys(cfg.board).forEach(col => { (cfg.board[col] || []).forEach(it => {
      if (it.who === name){
        const ci = Math.max(0, cols.indexOf(col));
        out.push({ id: it.id, t: it.t, prog: Math.min(95, Math.round((ci + 0.7) / cols.length * 100)),
          col: (cfg.cols[ci] || ['', col])[1], gate: !!it.merge });
      } }); });
  }
  if (task && !out.some(q => q.t === task || task.indexOf(q.id) >= 0))
    out.unshift({ id: '', t: task, prog: 35 + rpgHash(name + task) % 50, col: 'в работе', gate: false });
  /* мегапроект CEO: задача проекта — всегда первый квест исполнителя */
  if (typeof projTaskOf === 'function'){ const pt = projTaskOf(dept, name);
    if (pt && !out.some(q => q.t === pt.t)) out.unshift({ id:'🚀', t: pt.t,
      prog: pt.state === 'done' ? 100 : pt.state === 'blocked' ? 55 : pt.state === 'active' ? 62 : 8,
      col: MEGA_PROJECT.title, gate: pt.state === 'blocked' }); }
  return out.slice(0, 4);
}
